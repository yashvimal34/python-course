text = "Hello, Python!"
print(text[0:5]) # output: Hello
print(text[:5])  # output: Hello
print(text[7:])  # output: Python!
print(text[::2]) # output: Hlo yhn
print(text[-6:-1])  # output: ython (Negative indexing) e.g 14-1 = 13, now (n-1) means 13-1 = 12 [-6:12]


